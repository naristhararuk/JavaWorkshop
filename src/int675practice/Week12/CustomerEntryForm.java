    private void setForm() {
        Customer c = customers.get(current);
        txtCid.setText(""+c.getCustomerId());
        txtName.setText(c.getName());
        txtAddressLine1.setText(c.getAddressLine1());
        txtAddressLine2.setText(c.getAddressLine2());
        txtCity.setText(c.getCity());
        txtState.setText(c.getState());
        txtZip.setText(c.getZip());
        txtPhone.setText(c.getPhone());
        txtEmail.setText(c.getEmail());
    }
    
    private void setCustomer(Customer c) {
        c.setName(txtName.getText());
        c.setAddressLine1(txtAddressLine1.getText());
        c.setAddressLine2(txtAddressLine2.getText());
        c.setCity(txtCity.getText());
        c.setState(txtState.getText());
        c.setZip(txtZip.getText());
        c.setPhone(txtPhone.getText());
        c.setEmail(txtEmail.getText());
    }
    
    private void clearForm() {
        txtCid.setText("Auto ID");
        txtName.setText("");
        txtAddressLine1.setText("");
        txtAddressLine2.setText("");
        txtCity.setText("");
        txtState.setText("");
        txtZip.setText("");
        txtPhone.setText("");
        txtEmail.setText("");
    }
    
    private void refresh() {
        customers = Collections.unmodifiableList(Customer.findByName(""));
    }

