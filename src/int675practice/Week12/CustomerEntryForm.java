    private void jbPreviousActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbPreviousActionPerformed
        if (current>0) {
            current = current -1 ;
            setForm();
        }
    }//GEN-LAST:event_jbPreviousActionPerformed

    private void jbNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbNextActionPerformed
        if (current < (customers.size()-1)) {
            current = current +1 ;
            setForm();
        }
    }//GEN-LAST:event_jbNextActionPerformed

    private void jbNewCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbNewCustomerActionPerformed
        clearForm();
        jbNewCustomer.setEnabled(false);
        jbSave.setEnabled(true);
        isNewCustomer = true;
    }//GEN-LAST:event_jbNewCustomerActionPerformed

    private void jbSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbSaveActionPerformed
        Customer c = null;
        if (isNewCustomer) {
            isNewCustomer = false;
            c = new Customer();
            setCustomer(c);
            c.save();
            refresh();
            current = customers.size()-1;
        } else {
            c = customers.get(current);
            setCustomer(c);
            c.save();
        }
        setForm();
        jbSave.setEnabled(false);
        jbNewCustomer.setEnabled(true);
        jbSave.requestFocus();
    }//GEN-LAST:event_jbSaveActionPerformed

